map<array<int,3>, bool> cube;
int main() {
  ios_base::sync_with_stdio(false); cin.tie(NULL);
  string s;
  int x1, x2, y1, y2, z1, z2;
  while (cin >> s >> x1 >> x2 >> y1 >> y2 >> z1 >> z2) {
    for (int i = max(-50,x1); i <= min(50,x2); i++) {
      for (int j = max(-50,y1); j <= min(50,y2); j++) {
        for (int k = max(-50,z1); k <= min(50,z2); k++) {
          cube[{i, j, k}] = s == "on" ? 1 : 0;
        }
      }
    }
  }

  int cnt = 0;
  for (int i = -50; i <= 50; i++) {
    for (int j = -50; j <= 50; j++) {
      for (int k = -50; k <= 50; k++) {
        if (cube[{i, j, k}]) cnt++;
      }
    }
  }
  cout << cnt << endl;
}
