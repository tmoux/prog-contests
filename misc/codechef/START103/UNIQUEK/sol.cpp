int solve() {
  int N; cin >> N;
  int K; cin >> K;
  string s; cin >> s;
  vector<array<int, 2>> cnt(K);
  F0R(i, N) {
    cnt[i%K][s[i]-'0']++;
  }

  const int INF = 1e9;
  auto solve0 = [](int n0, int n1) -> int {
    if (n1 % 2 == 0) return n1/2;
    else return (n1+1)/2 + 1;
  };
  auto solve1 = [](int n0, int n1) -> int {
    if (n1 == 0) return INF;
    else return n0;
  };

  int cost_0 = 0, cost_1 = 0;
  F0R(i, K) {
    cost_0 = min(INF, cost_0 + solve0(cnt[i][0], cnt[i][1]));
    cost_1 = min(INF, cost_1 + solve1(cnt[i][0], cnt[i][1]));
  }

  return min(cost_0, cost_1);
}

int main() {
  ios_base::sync_with_stdio(false); cin.tie(NULL);
  int T; cin >> T;
  while (T--) cout << solve() << '\n';
}
