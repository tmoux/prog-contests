#include <bits/stdc++.h>
using namespace std;
using ll = long long;

const int maxn = 1e6+6, M = 1e9+7;
int n, p;
int k[maxn];

void solve() {
    cin >> n >> p;
    for (int i = 0; i < n; i++) {
        cin >> k[i];
    }
}

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int t; cin >> t;
    while (t--) solve();
}
