#include <bits/stdc++.h>
using namespace std;
using ll = long long;

const int maxn = 3e5+5;
int n, x, y, z, a[maxn];

void solve() {
    cin >> n >> x >> y >> z;
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
}

int main() {
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int t; cin >> t;
    while (t--) solve();
}
